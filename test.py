def swap(matrix, index_a, index_b):
    support = matrix[index_a]
    matrix[index_a] = matrix[index_b]
    matrix[index_b] = support

    return matrix

def array_comparison(matrix, matrix_length, index, previndex, index_of_index):
    current_row = 0
    next_row_to_compare = 1

    equals = []
    compare = []
    while (next_row_to_compare < matrix_length):
        #verifica se tutti i numeri precenti sono uguali
        if index_of_index > 0:
            compare.append(matrix[next_row_to_compare][previndex])
        if index_of_index > 0 and matrix[current_row][previndex] == matrix[next_row_to_compare][previndex]:
            equals.append(matrix[current_row][previndex])
        if (index_of_index == 0 and matrix[current_row][index] > matrix[next_row_to_compare][index]) or (equals == compare and matrix[current_row][index] > matrix[next_row_to_compare][index]):
            matrix = swap(matrix, current_row, next_row_to_compare)
        next_row_to_compare += 1
        current_row += 1

    return matrix

def matrix_iteration(index, matrix, matrix_length, previndex, index_of_index):
    next_row = 1
    
    while (next_row < len(matrix)):

        matrix = array_comparison(matrix, matrix_length, index, previndex, index_of_index)

        matrix_length -= 1
        next_row += 1
        
    return matrix

